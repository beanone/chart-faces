package org.javaq.chartfaces.constants;

/**
 * Enumeration of type of layout, typically applied to chart legends.
 * 
 * @author Hongyan Li
 * @since 1.0
 */
public enum EnumLayout {
	/**
	 * a layout that flows from left to right and then down.
	 */
	flow,
	/**
	 * a horizontal layout.
	 */
	horizontal,
	/**
	 * a vertical layout.
	 */
	vertical;
}